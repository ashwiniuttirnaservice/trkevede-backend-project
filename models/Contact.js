const mongoose = require("mongoose");

const ContactSchema = new mongoose.Schema(
    {

        name: {
            type: String,
            required: true,
            trim: true,
        },

        email: {
            type: String,
            required: true,
            trim: true,
        },

        mobile_no: {
            type: String,
            required: true,
            trim: true,
        },


        message: {
            type: String,
            required: true,
            trim: true,
        },

        isActive: {
            type: Boolean,
            default: true,
        },
    },
    { timestamps: true },
);

module.exports = mongoose.model("Contact", ContactSchema);
